    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String nombreUsuario = txtusuario.getText();
        String contrasena = new String(txtcontra.getPassword());

        try {
            // Obtener la conexión desde la clase ConexionOracle
            Connection conn = ConexionOracle.conectar();

            CallableStatement cs = conn.prepareCall("{call IniciarSesion(?, ?, ?)}");
            cs.setString(1, nombreUsuario);
            cs.setString(2, contrasena);
            cs.registerOutParameter(3, Types.VARCHAR);

            // Ejecutar la llamada al procedimiento almacenado
            cs.execute();

            // Obtener el resultado del procedimiento almacenado
            String resultado = cs.getString(3);
            System.out.println("Resultado del procedimiento almacenado: " + resultado);

            JOptionPane.showMessageDialog(this, resultado);

            // Cerrar la conexión
            cs.close();
            conn.close();

            if (resultado.startsWith("Inicio de sesión exitoso")) {
                Inicio homeFrame = new Inicio();
                homeFrame.setVisible(true);
                homeFrame.pack();
                homeFrame.setLocationRelativeTo(null);
                this.dispose();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

